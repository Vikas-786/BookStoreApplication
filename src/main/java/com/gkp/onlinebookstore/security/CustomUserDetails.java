package com.gkp.onlinebookstore.security;

import java.util.Collection;
import java.util.List;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import com.gkp.onlinebookstore.entity.Role;
import com.gkp.onlinebookstore.entity.User;

public class CustomUserDetails implements UserDetails {

	private User user;

	public CustomUserDetails(User user) {
		this.user = user;
	}

	@Override
	public Collection<? extends GrantedAuthority> getAuthorities() {
		return List.of(new SimpleGrantedAuthority("ROLE_" + user.getRole().name()));
	}

	@Override
	public String getPassword() {
		return user.getPassword();
	}

	@Override
	public String getUsername() {
		return user.getUsername();
	}

	@Override
	public boolean isAccountNonExpired() {
		return true;
	}

	@Override
	public boolean isAccountNonLocked() {
		return true;
	}

	@Override
	public boolean isCredentialsNonExpired() {
		return true;
	}

	@Override
	public boolean isEnabled() {
		return user.isEnabled();
	}

	public Role getRole() {
		return user.getRole();
	}
}


//class wraps around our custom User entity to adapt it to Spring Securityâ€™s needs.
//Integrates User Model with Spring Security.
//Implements UserDetails interface - part of Spring Security - Used to Retrieve the necessary user information
//implementing UserDetails, Spring Security can treat our User object as a security-related entity.